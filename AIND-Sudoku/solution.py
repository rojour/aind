
assignments = []
rows = 'ABCDEFGHI'
cols = '123456789'
def cross(A, B):
    "Cross product of elements in A and elements in B."
    return [s+t for s in A for t in B]

def diags(a, b):
    "Create the list of itms in the diagonal."
    return (a[s]+b[s] for s in range(9))

boxes = cross(rows, cols)
"Find the items in the diagonals. In the second case, you start the columns in reverse order"
left_diag = list(diags(rows, cols))
right_diag = list(diags(rows, cols[::-1]))

row_units = [cross(r, cols) for r in rows]
column_units = [cross(rows, c) for c in cols]
square_units = [cross(rs, cs) for rs in ('ABC','DEF','GHI') for cs in ('123','456','789')]
unitlist = row_units + column_units + square_units
units = dict((s, [u for u in unitlist if s in u]) for s in boxes)

"append the diagonals to the units in each of the boxes of the diagonals"
for box in left_diag:
    units[box].append(left_diag)

for box in right_diag:
    units[box].append(right_diag)


peers = dict((s, set(sum(units[s],[]))-set([s])) for s in boxes)

def assign_value(values, box, value):
    """
    Please use this function to update your values dictionary!
    Assigns a value to a given box. If it updates the board record it.
    """

    # Don't waste memory appending actions that don't actually change any values
    if values[box] == value:
        return values

    values[box] = value
    if len(value) == 1:
        assignments.append(values.copy())
    return values

def naked_twins(values):
    """Eliminate values using the naked twins strategy.
    Args:
        values(dict): a dictionary of the form {'box_name': '123456789', ...}

    Returns:
        the values dictionary with the naked twins eliminated from peers.
    """
    "Start with one unit at a time"
    for unit in unitlist:
        "Find all boxes that have two digits and make pairs of the ones that have the same digits"
        twin_values = [box for box in unit if len(values[box]) == 2]
        twin_tuples = ((twin_values[i], twin_values[j]) for i in range(len(twin_values)) for j in range(i+1, len(twin_values)) if(values[twin_values[i]] == values[twin_values[j]]))
        "With each of those pairs, and each box of the unit"
        for twin in twin_tuples:
            for box in unit:
                "values are not itself or have just one digit"
                if values[box] != values[twin[0]] and len(values[box]) != 1:
                    new_value = values[box]
                    "eliminate the digits not available"
                    for digit in values[twin[0]]:
                        new_value = new_value.replace(digit, '')
                        assign_value(values, box, new_value)

    return values
